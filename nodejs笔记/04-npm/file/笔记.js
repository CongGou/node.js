
/*

npm (Node Package Manager)


    npm init   /  npm init -y
        项目初始化

    npm install 包名  /  npm i 包名
        安装的时候需要带上一些参数
            --save /  -S   当前项目所依赖的包 生产环境--项目上线之后，还需要依赖的
            -g   全局安装
            --dev  /  -D   当前项目所依赖的包 开发环境--只在开发过程中依赖，项目上线之后就不用了

    npm uninstall 包名 / npm un 包含
        卸载的时候，请带上安装时候的参数

    npm update 包名
        也可以 包名@版本号


    npm publish
    npm unpublish
        上传/删除 npm服务器上的项目



    npm list
        展示当前安装的包



cnpm
    npm i cnpm -g

 */